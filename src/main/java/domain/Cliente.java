package domain;

public class Cliente {
    private String unNombre;
    private String unaReserva;

    public Cliente(String unNombre, String unaReserva) {
        this.unNombre = unNombre;
        this.unaReserva = unaReserva;
    }

    public String getUnNombre() { return unNombre; }

    public String getUnaReserva() { return unaReserva; }

}





